# Convert deck lists from https://onepiecetopdecks.com to a format readable by the simulator

import os
import pyperclip
import time

print("Please copy and paste your decklist from onepiecetopdecks.com.\n"
      "To convert, type \"done\" on a new line and press enter")
lines = []
fixed = []

stopword = "done"
while True:
    line = input()
    if line.strip().lower() == stopword:
        break
    lines.append(line)

os.system('cls')

for value in lines:
    converted = value.replace(' ', 'x').replace('(', '').replace(')','')
    fixed.append(converted)

final = ''
for x in fixed:
    final = final + x + "\n"

pyperclip.copy(final)
